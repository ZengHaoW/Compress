function [X, Y, Z, H] = generateSequences(x1, x2, x3, x4, l)
%GENERATESEQUENCES 根据初值生成四维混沌序列, 序列长度为图片尺寸M*N
%   此处显示详细说明
    h = 0.01;%设置步长
    NN = l + 5000;%运行混沌系统得到的序列个数, l为图像总像素个数，前5000个被舍去
    q = 0.95;%分数阶阶数
    z0 = [x1 x2 x3 x4];
    [~,y] = FrataSim(h,NN,z0,q);

    X = y(1,:);
    X = X(5001: 5000 + l);        %去除前3001项，获得更好的随机性（求解陈氏系统的子函数多计算了3000点）
    Y = y(2,:);
    Y = Y(5001: 5000 + l);
    Z = y(3,:);
    Z = Z(5001: 5000 + l);
    H = y(4,:);
    H = H(5001: 5000 + l);
end

